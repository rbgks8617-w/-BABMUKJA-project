function options(prefix: string, items: [string, number][]): MenuOption[] {
  return items.map(([name, price], index) => ({
    id: `${prefix}-${index + 1}`,
    name,
    price,
  }));
}

type MenuInput = Omit<Menu, "options" | "tasteScore" | "portionScore" | "valueScore"> &
  Partial<Pick<Menu, "tasteScore" | "portionScore" | "valueScore">> & {
  optionItems: [string, number][];
};

function menu({
  id,
  restaurantId,
  name,
  imageUrl,
  price,
  category,
  tags,
  description,
  recommendationText,
  optionItems,
  tasteScore = 4.3,
  portionScore = 4.3,
  valueScore = 4.4,
}: MenuInput): Menu {
  return {
    id,
    restaurantId,
    name,
    imageUrl,
    price,
    category,
    tags,
    description,
    recommendationText,
    tasteScore,
    portionScore,
    valueScore,
    options: options(id, optionItems),
  };
}

